package bricker.main;

/**
 * Enum of all the counter kinds.
 */
public enum CounterKinds {
    /**
     * Counter of the lives number.
     */
    LifeCounter,
    /**
     * Counter of the extra-paddle number.
     */
    ExtraPaddleCounter,
    /**
     * Counter of the bricks number.
     */
    BricksCounter
}
